package com.educomser.model;

/**
 *
 * @author alumno
 */
public class Estudiante {
    
    private int id;
    private String nombre;
    private int edad;
    
    public Estudiante(){
    }

    public int getId() {
        return id;
    }

    public void setId(int id) {
        this.id = id;
    }

    public String getNombre() {
        return nombre;
    }

    public void setNombre(String nombre) {
        this.nombre = nombre;
    }

    public int getEdad() {
        return edad;
    }

    public void setEdad(int edad) {
        this.edad = edad;
    }
    
    
}
